<script>
document.addEventListener('DOMContentLoaded', function () {
    var addButton = document.getElementById('addServiceTypeButton');
    var selector = document.getElementById('service_type_selector');
    var table = document.getElementById('jobServiceTypesTable');
    var billedAmountInput = document.getElementById('billed_amount');
    var servicePriceMap = <?= json_encode($servicePriceMap, JSON_HEX_TAG | JSON_HEX_APOS | JSON_HEX_QUOT | JSON_HEX_AMP) ?>;

    if (!addButton || !selector || !table || !billedAmountInput) {
        return;
    }

    var tableBody = table.querySelector('tbody');

    function updateBilledAmount() {
        var selectedInputs = tableBody.querySelectorAll('input[name="service_types[]"]');
        var total = 0;

        selectedInputs.forEach(function (input) {
            var row = input.closest('tr');
            if (!row) {
                return;
            }

            var priceInput = row.querySelector('input[name="service_prices[]"]');
            total += Number(priceInput ? priceInput.value : 0);
        });

        billedAmountInput.value = total.toFixed(2);
    }

    function removeEmptyRow() {
        var emptyRow = tableBody.querySelector('.job-service-types-empty-row');
        if (emptyRow) {
            emptyRow.remove();
        }
    }

    function ensureEmptyRow() {
        if (tableBody.querySelector('tr') !== null) {
            return;
        }

        var row = document.createElement('tr');
        row.className = 'job-service-types-empty-row';
        row.innerHTML = '<td colspan="3" class="text-center text-muted py-3">No service types added yet.</td>';
        tableBody.appendChild(row);
    }

    function addServiceTypeRow(serviceType) {
        if (!serviceType || tableBody.querySelector('tr[data-service-type="' + CSS.escape(serviceType) + '"]')) {
            return;
        }

        removeEmptyRow();

        var row = document.createElement('tr');
        row.setAttribute('data-service-type', serviceType);
        row.innerHTML = '<td>'
            + serviceType
            + '<input type="hidden" name="service_types[]" value="' + serviceType.replace(/&/g, '&amp;').replace(/"/g, '&quot;').replace(/</g, '&lt;').replace(/>/g, '&gt;') + '"></td>'
            + '<td><input class="form-control form-control-sm" name="service_prices[]" type="number" min="0" step="0.01" value="' + Number(servicePriceMap[serviceType] || 0).toFixed(2) + '"></td>'
            + '<td><button type="button" class="btn btn-outline-danger btn-sm" data-remove-service-type>Remove</button></td>';
        tableBody.appendChild(row);
        updateBilledAmount();
    }

    addButton.addEventListener('click', function () {
        addServiceTypeRow(selector.value);
        selector.value = '';
    });

    tableBody.addEventListener('click', function (event) {
        var target = event.target;
        if (!(target instanceof HTMLElement) || !target.hasAttribute('data-remove-service-type')) {
            return;
        }

        var row = target.closest('tr');
        if (row) {
            row.remove();
        }

        ensureEmptyRow();
        updateBilledAmount();
    });

    tableBody.addEventListener('input', function (event) {
        var target = event.target;
        if (!(target instanceof HTMLInputElement) || target.name !== 'service_prices[]') {
            return;
        }

        updateBilledAmount();
    });

    updateBilledAmount();
});

