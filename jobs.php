function coolopz_build_job_form_from_record(array $job, array $servicePriceMap): array
{
    $jobForm = [
        'ticket_number' => $job['ticket_number'],
        'customer_name' => $job['customer_name'],
        'service_lines' => coolopz_fetch_job_service_lines((int) $job['id']),
        'technician_team' => $job['technician_team'],
        'attending_technicians' => coolopz_parse_summary_names($job['attending_technicians'] ?? ''),
        'site_address' => $job['site_address'] ?? '',
        'google_maps_url' => $job['google_maps_url'] ?? '',
        'person_in_charge_name' => $job['person_in_charge_name'] ?? '',
        'person_in_charge_contact' => $job['person_in_charge_contact'] ?? '',
        'client_update_token' => $job['client_update_token'] ?? '',
        'status' => $job['status'],
        'priority_level' => $job['priority_level'],
        'billed_amount' => '0.00',
        'notes' => $job['notes'],
    ];

    if ($jobForm['service_lines'] === [] && $job['service_type'] !== '') {
        $fallbackLines = [];
        foreach (coolopz_normalize_service_names(explode(',', (string) $job['service_type'])) as $serviceName) {
            $fallbackLines[] = [
                'service_name' => $serviceName,
                'line_price' => $servicePriceMap[$serviceName] ?? 0,
            ];
        }

        $jobForm['service_lines'] = $fallbackLines;
    }

    $jobForm['billed_amount'] = number_format(coolopz_calculate_billed_amount($jobForm['service_lines']), 2, '.', '');

    return $jobForm;
}

function coolopz_uploaded_file_from_index(array $fileField, int|string $index): ?array
{
    if (!isset($fileField['error'][$index])) {
        return null;
    }

    return [
        'name' => $fileField['name'][$index] ?? '',
        'type' => $fileField['type'][$index] ?? '',
        'tmp_name' => $fileField['tmp_name'][$index] ?? '',
        'error' => $fileField['error'][$index] ?? UPLOAD_ERR_NO_FILE,
        'size' => $fileField['size'][$index] ?? 0,
    ];
}

